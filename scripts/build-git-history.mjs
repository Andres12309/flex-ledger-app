function git(args) {
  return execFileSync('git', args, {
    encoding: 'utf8',
    env: { ...process.env, ...AUTHOR },
    stdio: ['pipe', 'pipe', 'pipe'],
  });
}

function addPaths(paths) {
  const existing = paths.filter((p) => existsSync(p));
  if (existing.length === 0) return false;
  git(['add', '--', ...existing]);
  return true;
}

function commit(message, label, allowEmpty = false) {
  const staged = git(['diff', '--cached', '--name-only']).trim();
  const msgPath = join(tmpdir(), `git-commit-${Date.now()}.txt`);
  writeFileSync(msgPath, message, { encoding: 'utf8' });

  try {
    if (!staged) {
      if (allowEmpty) {
        git(['commit', '--allow-empty', '-F', msgPath]);
        const sha = git(['rev-parse', '--short', 'HEAD']).trim();
        console.log(`  ✓ ${sha}  ${label} (empty)`);
      } else {
        console.log(`  (skip vacío) ${label}`);
      }
      return;
    }
    git(['commit', '-F', msgPath]);
    const sha = git(['rev-parse', '--short', 'HEAD']).trim();
    console.log(`  ✓ ${sha}  ${label}`);
  } finally {
    try {
      unlinkSync(msgPath);
    } catch {
      /* ignore */
    }
  }
}

function runCommits(commits) {
  commits.forEach(({ msg, paths, allowEmpty }, i) => {
    const label = msg.split('\n')[0];
    console.log(`\n[${i + 1}/${commits.length}] ${label}`);
    if (paths?.length) addPaths(paths);
    commit(msg, label, allowEmpty);
  });
}

function hasCommits() {
  try {
    git(['rev-parse', 'HEAD']);
    return true;
  } catch {
    return false;
  }
}

